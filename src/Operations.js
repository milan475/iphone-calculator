export function sum(val1, val2) {
    return parseFloat(val1) + parseFloat(val2);
}

export function substract(val1, val2) {
    return parseFloat(val1) - parseFloat(val2);
}

export function product(val1, val2) {
    return parseFloat(val1) * parseFloat(val2);
}

export function devide(val1, val2) {
    return parseFloat(val1) / parseFloat(val2);
}